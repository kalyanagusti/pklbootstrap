        <h5><center>
        Ruwatan merupakan Prosesi Penyucian yang sudah sangat lekat dengan kebudayaan dan adat di Jawa. 
        Ruwatan Bocah Rambut Gimbal kurang lebih memiliki maksud yang sama, yakni suatu upacara atau ritual 
        yang bertujuan untuk mengusir nasib buruk atau kesialan baik pada si bocah Gimbal maupun masyarakat Dieng Pada umumnya.
        Bocah berambut Gimbal sendiri merupakan sebuah fenomena unik yang sudah ada di Dieng sedari dahulu kala,
        Dimana anak-anak tertentu yang berusia antara 40 hari hingga 6 tahun tumbuh rambut gimbal di kepalanya secara alami.
        Masyarakat Dataran Tinggi Dieng percaya bahwa anak-anak berambut Gimbal tersebut merupakan titipan dari Kyai Kolo Dete.
        Kyai Kolo Dete merupakan salah seorang Pejabat / Punggawa di masa Mataram Islam (pada masa abad ke empatbelas)
        yang ditugaskan untuk mempersiapkan pemerintahan di wilayah Dataran Tinggi Dieng. Tiba di Dataran Tinggi Dieng,
        Kyai Kolo Dete dan istrinya (Nini Roro Rence) mendapat wahyu dari Ratu Pantai Selatan. Pasangan ini ditugaskan
        membawa masyarakat Dieng menuju kesejahteraan. Tolak ukur sejahteranya masyarakat Dieng akan ditandai dengan keberadaan
        anak-anak berambut gimbal. Sejak itulah, muncul anak-anak berambut gimbal di kawasan Dataran Tinggi Dieng. Bagi masyarakat 
        Dataran Tinggi Dieng, jumlah anak berambut gimbal berkorelasi dengan kesejahteraan masyarakat. Semakin banyak jumlah anak 
        berambut gimbal, masyarakat Dieng yakin kesejahteraan mereka akan semakin baik. Begitu pula sebaliknya.
        Munculnya rambut gimbal pada seorang anak akan ditandai dengan panas tubuh yang tinggi selama beberapa hari. Suhu tubuh
        anak tersebut akan normal dengan sendirinya pada pagi hari, bersamaan dengan munculnya rambut gimbal di kepala sang anak. 
        Dalam kehidupan sehari-hari, seorang anak berambut gimbal tidak berbeda dengan anak-anak lainnya. Mereka bermain bersama
        dengan anak-anak lain. Hanya saja, anak berambut gimbal biasanya cenderung lebih aktif dibanding anak-anak lain.
